package com.binbit.resiliencia.model;

public class PartnerDetails {

	public Integer id;
	public Integer partnerRoleId;
	public String  password;
	public Integer opId;
	public String countryId;
	public Integer carrierId;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getPartnerRoleId() {
		return partnerRoleId;
	}
	public void setPartnerRoleId(Integer partnerRoleId) {
		this.partnerRoleId = partnerRoleId;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public Integer getOpId() {
		return opId;
	}
	public void setOpId(Integer opId) {
		this.opId = opId;
	}
	public String getCountryId() {
		return countryId;
	}
	public void setCountryId(String countryId) {
		this.countryId = countryId;
	}
	public Integer getCarrierId() {
		return carrierId;
	}
	public void setCarrierId(Integer carrierId) {
		this.carrierId = carrierId;
	}
	
	
	
}
